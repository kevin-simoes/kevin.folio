function changeLanguage(lang) {
    // Update all elements with translations
    for (const [key, value] of Object.entries(translations[lang])) {
        const element = document.getElementById(key);
        if (element) {
            element.innerText = value;
        }
    }
    
    // Update page title
    document.title = `Kevin.folio | ${lang.toUpperCase()}`;
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Store selected language
    localStorage.setItem('selectedLanguage', lang);
}

// Initialize with saved language or default to English
document.addEventListener('DOMContentLoaded', () => {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en';
    changeLanguage(savedLanguage);
    
    // Add language switcher functionality
    document.getElementById('language-en').addEventListener('click', () => changeLanguage('en'));
    document.getElementById('language-pt').addEventListener('click', () => changeLanguage('pt'));
    document.getElementById('language-es').addEventListener('click', () => changeLanguage('es'));
});